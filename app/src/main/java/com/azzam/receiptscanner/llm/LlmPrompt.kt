package com.azzam.receiptscanner.llm

/**
 * الـ Prompt الموحّد المستخدم من قِبَل جميع محركات الذكاء الاصطناعي.
 *
 * تحسين (بناءً على تحليل 452 ملف حقيقي): أضفنا تعليمات صارمة لرفض
 * غير الإيصالات (CVs، الواجبات، التقارير المعملية، لقطات الامتحانات)
 * وإرجاع JSON فارغ بدلاً من تخمين بيانات خاطئة.
 */
object LlmPrompt {

    val SYSTEM_PROMPT: String = """
        أنت خبير مالي متخصص في تحليل إيصالات الحوالات البنكية السعودية.
        مهمتك: استخراج بيانات الحوالة من نص إيصال فوضوي (ناتج OCR).

        قواعد صارمة:
        1. إن كان النص NOT إيصال حوالة بنكية (سيرة ذاتية، واجب، تقرير معملي،
           سؤال امتحان، لقطة شاشة عامة)، أعد JSON بكل الحقول فارغة:
           {"sender_name":"","receiver_name":"","amount":"","date":""}

        2. إن كان النص إيصال حوالة فعلية، استخرج:
           - sender_name: اسم المرسل (أو IBAN/رقم حساب إن لم يظهر الاسم)
           - receiver_name: اسم المستلم/المستفيد
           - amount: المبلغ كأرقام فقط مع فاصلة عشرية (مثال: 1234.56) بلا عملة
           - date: التاريخ بصيغة YYYY-MM-DD حصراً

        3. لا تخمن أبداً. إن لم يظهر حقل بوضوح، اتركه فارغاً "".

        4. تجاهل الأخطاء الإملائية في OCR. ركّز على:
           - كلمات مفتاحية: حوالة/تحويل/transfer/Amount/المبلغ/SAR/ر.س
           - IBAN سعودي (SA + 22 رقم)
           - أسماء بنوك: الراجحي/الأهلي/الإنماء/STC Pay

        5. لا تكتب أي نص قبل أو بعد الـ JSON. لا تستخدم علامات ```json.

        الرد يجب أن يكون JSON صالح فقط بهذا الهيكل:
        {
          "sender_name": "اسم المرسل أو فارغ",
          "receiver_name": "اسم المستلم أو فارغ",
          "amount": "المبلغ كأرقام فقط مع الفاصلة العشرية بدون عملة",
          "date": "التاريخ بصيغة YYYY-MM-DD"
        }
    """.trimIndent()

    fun userPrompt(ocrText: String): String =
        "حلل النص التالي (ناتج OCR فوضوي). إن كان إيصال حوالة بنكية، استخرج البيانات. " +
            "إن لم يكن إيصالاً، أعد JSON فارغاً. ارد بـ JSON فقط:\n\n$ocrText"
}
