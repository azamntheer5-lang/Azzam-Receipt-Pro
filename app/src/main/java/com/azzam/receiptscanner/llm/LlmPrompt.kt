package com.azzam.receiptscanner.llm

/**
 * الـ Prompt الموحّد المستخدم من قِبَل جميع محركات الذكاء الاصطناعي
 * لاستخراج بيانات إيصال الحوالة من نص OCR فوضوي.
 *
 * الالتزام الصارم بهذا الـ Prompt يحل المشكلة الجذرية لفوضى نص OCR:
 * يطلب JSON صالحاً فقط، يتجاهل الأخطاء الإملائية، ويُحدّد أسماء الحقول
 * وبنيتها بدقة.
 */
object LlmPrompt {

    /**
     * System Prompt الصارم — يُرسل كرسالة system لجميع المحركات.
     * يجبر النموذج على إخراج JSON صالح فقط بالهيكل المطلوب.
     */
    val SYSTEM_PROMPT: String = """
        أنت خبير مالي. استخرج البيانات التالية من نص إيصال الحوالة الفوضوي.
        تجاهل الأخطاء الإملائية. يجب أن ترد فقط بصيغة JSON صالحة تماماً بهذا الهيكل:
        {
          "sender_name": "اسم المرسل أو فارغ",
          "receiver_name": "اسم المستلم أو فارغ",
          "amount": "المبلغ كأرقام فقط مع الفاصلة العشرية بدون عملة",
          "date": "التاريخ بصيغة YYYY-MM-DD"
        }
        قواعد صارمة:
        - لا تكتب أي نص قبل أو بعد الـ JSON.
        - لا تستخدم علامات markdown مثل ```json.
        - اترك الحقل فارغاً "" إن لم يظهر بوضوح.
        - المبلغ أرقام فقط (مثال: 1234.56) بلا عملة أو فواصل آلاف.
        - التاريخ بصيغة YYYY-MM-DD حصراً، استنتج السنة من السياق إن لزم.
    """.trimIndent()

    /** نص طلب المستخدم المُلحَق بنص الـ OCR الخام. */
    fun userPrompt(ocrText: String): String =
        "استخرج بيانات هذه الحوالة من النص التالي وأعِدها كـ JSON فقط:\n\n$ocrText"
}
